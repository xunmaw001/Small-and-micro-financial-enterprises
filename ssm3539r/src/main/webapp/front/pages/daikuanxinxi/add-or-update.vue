<script>
	import wPicker from "@/components/w-picker/w-picker.vue";
    import xiaEditor from '@/components/xia-editor/xia-editor';
	export default {
		data() {
			return {
				cross:'',
				ruleForm: {
				daikuanmingcheng: '',
				daikuanzhonglei: '',
				fengmian: '',
				kedaijine: '',
				daikuantiaojian: '',
				haikuanqixian: '',
				xiangxijieshao: '',
				faburiqi: '',
				},
				daikuanzhongleiOptions: [],
				daikuanzhongleiIndex: 0,
				// 登陆用户信息
				user: {},
                ro:{
                   daikuanmingcheng : false,
                   daikuanzhonglei : false,
                   fengmian : false,
                   kedaijine : false,
                   daikuantiaojian : false,
                   haikuanqixian : false,
                   xiangxijieshao : false,
                   faburiqi : false,
                },
			}
		},
		components: {
			wPicker,
            xiaEditor
		},
		computed: {
			baseUrl() {
				return this.$base.url;
			},



		},
		async onLoad(options) {
            this.ruleForm.faburiqi = this.$utils.getCurDate();
			let table = uni.getStorageSync("nowTable");
			// 获取用户信息
			let res = await this.$api.session(table);
			this.user = res.data;
			
			// ss读取


			// 下拉框
			res = await this.$api.option(`daikuanzhonglei`,`daikuanzhonglei`,{});
			this.daikuanzhongleiOptions = res.data;
            this.daikuanzhongleiOptions.unshift("请选择贷款种类");

			// 如果有登陆，获取登陆后保存的userid
			this.ruleForm.userid = uni.getStorageSync("userid")
			if (options.refid) {
				// 如果上一级页面传递了refid，获取改refid数据信息
				this.ruleForm.refid = options.refid;
				this.ruleForm.nickname = uni.getStorageSync("nickname");
			}
			// 如果是更新操作
			if (options.id) {
				this.ruleForm.id = options.id;
				// 获取信息
				res = await this.$api.info(`daikuanxinxi`, this.ruleForm.id);
				this.ruleForm = res.data;
			}
			// 跨表
			this.cross = options.cross;
			if(options.cross){
				var obj = uni.getStorageSync('crossObj');
				for (var o in obj){
					if(o=='daikuanmingcheng'){
					this.ruleForm.daikuanmingcheng = obj[o];
					this.ro.daikuanmingcheng = true;
					continue;
					}
					if(o=='daikuanzhonglei'){
					this.ruleForm.daikuanzhonglei = obj[o];
					this.ro.daikuanzhonglei = true;
					continue;
					}
					if(o=='fengmian'){
					this.ruleForm.fengmian = obj[o].split(",")[0];
					this.ro.fengmian = true;
					continue;
					}
					if(o=='kedaijine'){
					this.ruleForm.kedaijine = obj[o];
					this.ro.kedaijine = true;
					continue;
					}
					if(o=='daikuantiaojian'){
					this.ruleForm.daikuantiaojian = obj[o];
					this.ro.daikuantiaojian = true;
					continue;
					}
					if(o=='haikuanqixian'){
					this.ruleForm.haikuanqixian = obj[o];
					this.ro.haikuanqixian = true;
					continue;
					}
					if(o=='xiangxijieshao'){
					this.ruleForm.xiangxijieshao = obj[o];
					this.ro.xiangxijieshao = true;
					continue;
					}
					if(o=='faburiqi'){
					this.ruleForm.faburiqi = obj[o];
					this.ro.faburiqi = true;
					continue;
					}
				}
			}
			this.styleChange()
		},
		methods: {
            xiangxijieshaoChange(e) {
                this.ruleForm.xiangxijieshao = e
            },
			styleChange() {
				this.$nextTick(()=>{
					// document.querySelectorAll('.app-update-pv . .uni-input-input').forEach(el=>{
					//   el.style.backgroundColor = this.addUpdateForm.input.content.backgroundColor
					// })
				})
			},

			// 多级联动参数

			faburiqiChange(e) {
				this.ruleForm.faburiqi = e.target.value;
				this.$forceUpdate();
			},


			// 下拉变化
			daikuanzhongleiChange(e) {
				this.daikuanzhongleiIndex = e.target.value
				this.ruleForm.daikuanzhonglei = this.daikuanzhongleiOptions[this.daikuanzhongleiIndex]
			},

			fengmianTap() {
				let _this = this;
				this.$api.upload(function(res) {
					_this.ruleForm.fengmian = 'upload/' + res.file;
					_this.$forceUpdate();
					_this.$nextTick(()=>{
						_this.styleChange()
					})
				});
			},

			getUUID () {
				return new Date().getTime();
			},
			async onSubmitTap() {

















//跨表计算判断
				var obj;
				if((!this.ruleForm.daikuanmingcheng)){
					this.$utils.msg(`贷款名称不能为空`);
					return
				}
				if((!this.ruleForm.daikuanzhonglei)){
					this.$utils.msg(`贷款种类不能为空`);
					return
				}
				if((!this.ruleForm.kedaijine)){
					this.$utils.msg(`可贷金额不能为空`);
					return
				}
				if(this.ruleForm.kedaijine&&(!this.$validate.isIntNumer(this.ruleForm.kedaijine))){
					this.$utils.msg(`可贷金额应输入整数`);
					return
				}
				if((!this.ruleForm.daikuantiaojian)){
					this.$utils.msg(`贷款条件不能为空`);
					return
				}
				//更新跨表属性
			       var crossuserid;
			       var crossrefid;
			       var crossoptnum;
				if(this.cross){
					var statusColumnName = uni.getStorageSync('statusColumnName');
					var statusColumnValue = uni.getStorageSync('statusColumnValue');
					if(statusColumnName!='') {
                        if(!obj) {
						    obj = uni.getStorageSync('crossObj');
                        }
						if(!statusColumnName.startsWith("[")) {
							for (var o in obj){
								if(o==statusColumnName){
									obj[o] = statusColumnValue;
								}

							}
							var table = uni.getStorageSync('crossTable');
							await this.$api.update(`${table}`, obj);
						} else {
						       crossuserid=Number(uni.getStorageSync('userid'));
						       crossrefid=obj['id'];
						       crossoptnum=uni.getStorageSync('statusColumnName');
						       crossoptnum=crossoptnum.replace(/\[/,"").replace(/\]/,"");
						}
					}
				}
				if(crossrefid && crossuserid) {
					this.ruleForm.crossuserid=crossuserid;
					this.ruleForm.crossrefid=crossrefid;
					let params = {
						page: 1,
						limit:10,
						crossuserid:crossuserid,
						crossrefid:crossrefid,
					}
					let res = await this.$api.list(`daikuanxinxi`, params);
					if (res.data.total >= crossoptnum) {
						this.$utils.msg(uni.getStorageSync('tips'));
						return false;
					} else {
                //跨表计算
						if(this.ruleForm.id){
							await this.$api.update(`daikuanxinxi`, this.ruleForm);
						}else{
							await this.$api.add(`daikuanxinxi`, this.ruleForm);
						}
						this.$utils.msgBack('提交成功');
					}
				} else {
                //跨表计算
					if(this.ruleForm.id){
						await this.$api.update(`daikuanxinxi`, this.ruleForm);
					}else{
						await this.$api.add(`daikuanxinxi`, this.ruleForm);
					}
					this.$utils.msgBack('提交成功');
				}
			},
			optionsChange(e) {
				this.index = e.target.value
			},
			bindDateChange(e) {
				this.date = e.target.value
			},
			getDate(type) {
				const date = new Date();
				let year = date.getFullYear();
				let month = date.getMonth() + 1;
				let day = date.getDate();
				if (type === 'start') {
					year = year - 60;
				} else if (type === 'end') {
					year = year + 2;
				}
				month = month > 9 ? month : '0' + month;;
				day = day > 9 ? day : '0' + day;
				return `${year}-${month}-${day}`;
			},
			toggleTab(str) {
				this.$refs[str].show();
			}
		}
	}
